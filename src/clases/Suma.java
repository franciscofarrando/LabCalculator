package clases;

public class Suma {

            // sumar dos numeros dobles reales

            public static double sumar ( double a, double b){
                return a + b;
            }

            // sumar dos numeros enteros

            public static int sumar ( int a, int b){
                return a + b;
            }

            // sumar tres numeros dobles reales

            public static double sumar ( double a, double b, double c){
                return a + b + c;
            }
            //valor acumulado

            public static double sumarvalorAcumulado ( double valor){
                return 0.0;
            }
}