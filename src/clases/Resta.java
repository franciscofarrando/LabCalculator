package clases;

public class Resta {

    // restar dos numeros dobles reales

    public static double restar ( double a, double b){
        return a - b;
    }

    // restar dos numeros enteros

    public static int restar ( int a, int b){
        return a - b;
    }

    // restar tres numeros dobles reales

    public static double restar ( double a, double b, double c){
        return a - b - c;
    }

    //valor acumulado

    public static double restarvalorAcumulado ( double valor){
        return 0.0;
    }
}
